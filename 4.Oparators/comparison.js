// Equal to (==)

console.log(5=='5');
console.log(5==5);
console.log(5==10);

//strict Equal to (===)
console.log(5===5);
console.log(5===55);
console.log(5 ==='5');

// Not Equal to (!=)
console.log(5 !='5');
console.log(5 != 6);

// Strict Not Equal to (!==)
console.log(5!=='5');
console.log(5!==6);
console.log(5!==5);

// greater than (>)
console.log(5>6);
console.log(6>5);

// less than (<)
console.log(5<6);
console.log(6<5);

// greater than or equal(>=)
console.log(5>=5);
console.log(5>=4);
console.log(5>=6);

// less than or equal(<=)
console.log(5<=5);
console.log(5<=6);
console.log(5<=4);